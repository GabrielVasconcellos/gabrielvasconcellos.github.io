<p>
	Inspired by childhood pet care games, Tamapote introduces the Bibitos, animal-plant hybrids that
	need a lot of love, care, and attention! Depending on how you take care of your Bibito, it can
	evolve into different adult forms and grow new Bibitos.
</p>
<h3 id="responsibilities">Responsibilities</h3>
<p>Game Development in Unity C#</p>
<ul>
	<li>
		<strong>Pet basic needs:</strong>
		<ul>
			<li>
				System to manage the current state of each basic need: hunger, sleep, affection, hygiene and
				health.
			</li>
			<li>
				Player interactions to replenish needs: using food items, pet with swipe motion, bathing and
				toggle room light for sleep or waking the pet up.
			</li>
			<li>
				Events depending on basic needs level. For example, speech bubbles asking for food and even
				running away from home.
			</li>
		</ul>
	</li>
	<li>
		<strong>Evolution:</strong>
		<ul>
			<li>
				Developed pet aging system: when the pet reaches a certain age, it will evolve based on
				different constraints like main food types used for feeding.
			</li>
		</ul>
	</li>
	<li>
		<strong>In-game store and inventory:</strong>
		<ul>
			<li>Currency system and interface for buying and listing items at the store.</li>
			<li>Player inventory to list and use items bought.</li>
			<li>Different items categories such as rooms, foods, hygiene items and accessories.</li>
			<li>
				Consumable and permanent items. A permanent item can be equipped and unequipped, having
				impact on how basic needs meters increase or decrease, while consumables are used only once
				and have an instantaneous effect, increase or decreasing a need meter.
			</li>
		</ul>
	</li>
	<li>
		<strong>User Interface (UI):</strong>
		<ul>
			<li>
				Implemented menus for navigating between rooms, opening the store or inventory and playing
				mini-games.
			</li>
		</ul>
	</li>
</ul>
