<p>
	This game was developed in just <strong>two days</strong> as part of a game jam, with the theme of
	<strong>repair</strong>. As the player, you assume the role of a high-ranking spaceship pilot
	defending three space stations from waves of enemies. The goal is to protect the human colony for
	as long as possible.
</p>

<h3>Key Mechanics:</h3>
<ul>
	<li>
		<strong>Defense & Repair:</strong> Players must defend the stations and repair any damage inflicted
		by enemies. Repairs require gathering specific materials.
	</li>
	<li>
		<strong>Resource Management:</strong> Red, green, and blue materials, dropped by defeated enemies,
		are used to repair their respective colored stations.
	</li>
	<li>
		<strong>Strategic Combat:</strong> Players need to balance defending the stations with collecting
		repair materials, making fast-paced decisions to keep the colony safe.
	</li>
</ul>

<p>
	This project was a showcase of quick prototyping, time management, and collaboration under a tight
	deadline, while still delivering a fun and engaging gameplay experience.
</p>
