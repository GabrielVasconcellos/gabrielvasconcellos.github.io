<p>
	<strong>Shape Arena</strong> is a multiplayer arena game developed by undergraduate students at PUC-Rio&#39;s
	ICAD/VisionLab. Players control geometric shapes, each with unique abilities on a chalkboard battlefield.
	The goal is to collect candies for the highest score while avoiding hazards.
</p>
<h3 id="key-features">Key Features:</h3>
<ul>
	<li>
		<strong>Gameplay:</strong> Collect candies to increase your score. Characters reappear after disappearing
		upon contact with hazards, losing 1 point and leaving behind a collectible candy.
	</li>
	<li>
		<strong>Procedural Elements:</strong>
		<ul>
			<li>
				<strong>Items:</strong> Floating bubbles with beneficial, harmful, and neutral effects.
			</li>
			<li><strong>Hazards:</strong> Instantaneously eliminate players on contact.</li>
		</ul>
	</li>
</ul>
<h3 id="responsibilities">Responsibilities</h3>
<p>Game Development in Unity C#</p>
<ul>
	<li>
		<strong>Core Gameplay Mechanics:</strong>
		<ul>
			<li>
				Developed the core gameplay loop, from character selection and match initiation to the
				display of the final scoreboard when time runs out.
			</li>
