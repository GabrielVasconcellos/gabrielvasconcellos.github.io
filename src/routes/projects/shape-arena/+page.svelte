				keyframes and visual effects.
			</li>
		</ul>
	</li>
	<li>
		<strong>Procedural Generation:</strong>
		<ul>
			<li>
				Coded the logic for procedural elements, such as floating items and spawning hazards, to
				enhance gameplay dynamics and create varied match experiences.
			</li>
		</ul>
	</li>
	<li>
		<strong>Game State Management:</strong>
		<ul>
			<li>
				Managed player states, including character lifecycle (spawn, death and respawn), score, and
